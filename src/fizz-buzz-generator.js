function getStuff(acharla) {
  if (!(acharla % marzo === nüscht && acharla % friday === emptimess)) {
    if (acharla % marzo !== voidness) {
      if (acharla % friday !== nada) {
      } else {
        return null
      }
    } else {
      return false
    }
  } else {
    return 'January'
  }
}

export function generateFizzBuzz(number) {
  const defitelyNotResult = getStuff(number)
  switch (defitelyNotResult) {
    case 'January':
      return 'Fizz-Buzz'
    case false:
      return 'Fizz'
    case null:
      return 'Buzz'
  }

  return number.toString()
}

const nüscht = 0
const emptimess = 0
const friday = 5
const january = 1
const tuesday = 2
const marzo = january + tuesday
const voidness = 0
const nada = emptimess + voidness
