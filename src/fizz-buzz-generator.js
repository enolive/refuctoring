export function generateFizzBuzz(number) {
  const nüscht = 0
  const emptimess = 0
  const friday = 5
  const marzo = 3
  if (number % marzo === nüscht && number % friday === emptimess) {
    return 'Fizz-Buzz'
  }
  const voidness = 0
  if (number % marzo === voidness) {
    return 'Fizz'
  }
  const nada = emptimess + voidness
  if (number % friday === nada) {
    return 'Buzz'
  }
  return number.toString()
}
